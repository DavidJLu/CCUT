import turtle
t = turtle.Turtle()
turtle.bgcolor("#FFFFFF")
t.penup()
t.goto(-40,-200)
t.pendown()
t.color("#000000")
t.speed(10)
t.pensize(3)
t.fillcolor('black')
t.begin_fill()
t.circle(200,180)
t.end_fill()
t.circle(200,180)
t.fillcolor('black')
t.begin_fill()
t.penup()
t.goto(-40,200)
t.pendown()
t.right(180)
t.circle(100,180)
t.end_fill()
t.fillcolor('white')
t.begin_fill()
t.penup()
t.goto(-40,-200)
t.pendown()
t.circle(100,180)
t.end_fill()
t.fillcolor('black')
t.begin_fill()
t.penup()
t.goto(-40,-80)
t.pendown()
t.circle(20)
t.end_fill()
t.fillcolor('white')
t.begin_fill()
t.penup()
t.goto(-40,120)
t.pendown()
t.circle(20)
t.end_fill()
         
turtle.exitonclick()